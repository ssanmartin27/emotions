import { useEffect, useState } from "react";
import { isSameDay } from "date-fns";
import { useCalendar } from "~/components/calendar-context";
import { formatTime } from "~/components/helpers";

export function CalendarTimeline() {
	const { use24HourFormat, selectedDate } = useCalendar();
	const [currentTime, setCurrentTime] = useState(new Date());

	useEffect(() => {
		const timer = setInterval(() => setCurrentTime(new Date()), 60 * 1000);
		return () => clearInterval(timer);
	}, []);

	// Only show timeline if selected date is today
	const isToday = isSameDay(selectedDate, currentTime);
	if (!isToday) {
		return null;
	}

	const getCurrentTimePosition = () => {
		const minutes = currentTime.getHours() * 60 + currentTime.getMinutes();
		return (minutes / 1440) * 100;
	};

	const formatCurrentTime = () => {
		return formatTime(currentTime, use24HourFormat);
	};

	return (
		<div
			className="pointer-events-none absolute inset-x-0 z-50 border-t-2 border-dashed border-rose-500/60"
			style={{ top: `${getCurrentTimePosition()}%` }}
		>
			<div className="absolute -left-1.5 -top-1.5 size-3 rounded-full bg-background border-2 border-rose-500"></div>

			<div className="absolute -left-18 flex w-16 -translate-y-1/2 justify-end bg-background pr-1 text-xs font-medium text-rose-600 dark:text-rose-400">
				{formatCurrentTime()}
			</div>
		</div>
	);
}
